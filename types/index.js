//js is dynamically typed language
// you don't tell the engine what type of data a variable holds
// it figures it out while your code is running
// variables can hold different types of values because it's all figured out during execution

// Primitive Types
// a type of data that represents a single value
// that is not an object ,and it is immutable (cannot be changed)
//there are six primitive types in js

// 1-undefined represents lack of existence (you shouldn't set a variable to this)
const a = undefined;

// 2-null represents lack of existence (you can set a variable to this)
const b = null;

// 3-boolean true or false
const c = true;

// 4-number floating point number (there's always some decimal) unlike other programming languages
// there is only one number type in js and it can make math weird
const d = 1;

// 5-string a sequence of characters both '' and "" can be used
const e = 'hello';

